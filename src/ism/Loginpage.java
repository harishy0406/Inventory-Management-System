    private boolean validate_data(String a, String b){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ims","root","");
            PreparedStatement pst = con.prepareStatement("select* from login where username=? and password=?");
            pst.setString(1, a);
            pst.setString(2,b);
            ResultSet rs = pst.executeQuery();
            if (rs.next())
                return true;
            else
                return false;
        }
        catch(Exception e){
            System.out.println(e);
            return false;
        }
    }
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField2.getText().length()==0)
            JOptionPane.showMessageDialog(null,"ENTER USERNAME");
        else if(jPasswordField1.getText().length()==0)
            JOptionPane.showMessageDialog(null,"ENTER PASSWORD");
        else{
            String a = jTextField2.getText();
            String b = jPasswordField1.getText();
            if(validate_data(a,b)){
                JOptionPane.showMessageDialog(null,"LOGIN SUCCESSFULL");
                Mainpage MainpageFrame = new Mainpage();
                MainpageFrame.setVisible(true);
                MainpageFrame.pack();
                MainpageFrame.setLocationRelativeTo(null);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"INCORRECT USERNAME OR PASSWORD");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

